#include <stdio.h>

int	ft_atoi(char *str)
{
	int n;
	int i;
	int r;

	i = 0;
	n = 0;
	r = 0;
	while ((str[i] > 0 && str[i] <= 32))
		i++;
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			n++;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		r = (r * 10) + (str[i] - 48);
		i++;
	}
	if (n % 2 != 1)
		return (r);
	return (-r);
}

int main()
{
	printf("%d", ft_atoi("   -----123456"));
	return (0);
}
