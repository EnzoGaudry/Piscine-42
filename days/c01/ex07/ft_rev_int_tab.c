void	ft_rev_int_tab(int *tab, int size)
{
	int ex;
	int i;
	int j;

	i = 0;
	while (i < size)
	{
		j = 0;
		while (j < size - 1)
		{
			if (tab[j] > tab[j + 1])
			{
				ex = tab[j + 1];
				tab[j + 1] = tab[j];
				tab[j] = ex;
			}
			j++;
		}
		i++;
	}
}
