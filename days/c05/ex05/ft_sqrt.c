#include <stdio.h>

int	ft_sqrt(int nb)
{
	long int i;

	i = 1;
	while (i * i < nb)
		i++;
	if (i * i == nb)
		return (i);
	else
		return (0);
}

int main()
{
	printf("%d", ft_sqrt(2147483647 * 2147483647));
			return (0);
}
